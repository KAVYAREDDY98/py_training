#INTEGERS

num_1 = '20'
num_2 = '10'
#ADDITION
result = int(num_1) + int(num_2)
print(f"The addition of '{num_1}' and '{num_2}' is '{result}'")

result = "The addition of '{}' and '{}' is '{}'".format(num_1,num_2, int(num_1)+int(num_2))
print(result)

#SUBTRACTION 
result = int(num_1) - int(num_2)
print(f"The subtraction of '{num_1}' and '{num_2}' is '{result}'")
result = "The subtraction of '{}' and '{}' is '{}'".format(num_1,num_2,int(num_1)-int(num_2))
print(result)

#MULTIPLICATION
result = int(num_1) * int(num_2)
print(f"The multiplication of '{num_1}' and '{num_2}' is '{result}'")

result = "The multiplication of '{}' and '{}' is '{}'".format(num_1,num_2,int(num_1)*int(num_2))
print(result)

#DIVISION
result = int(num_1) / int(num_2)
print(f"The division of '{num_1}' and '{num_2}' is '{result}'")

result = "The division of '{}' and '{}' is '{}'".format(num_1,num_2, int(num_1)/int(num_2))
print(result)

#MODULO
result = int(num_1) % int(num_2)
print(f"The modulo of '{num_1}' and '{num_2}' is '{result}'")

result = "The modulo of '{}' and '{}' is '{}'".format(num_1,num_2,int(num_1)%int(num_2))
print(result)

#EXPONENT
result = int(num_1) ** int(num_2)
print(f"The exponent of '{num_1}' and '{num_2}' is '{result}'")

result = "The exponent of '{}' and '{}' is '{}'".format(num_1,num_2, int(num_1)**int(num_2))
print(result)

#FLOOR DIVISION
result = int(num_1) // int(num_2)
print(f"The floor division of '{num_1}' and '{num_2}' is '{result}'")

result = "The floor division of '{}' and '{}' is '{}'".format(num_1,num_2, int(num_1)//int(num_2))
print(result)

#NEGATION
result = -(int(num_1)) + -(int(num_2))
print(f"The negation of '{num_1}' and '{num_2}' is '{result}'")

result = "The negation of '{}' and '{}' is '{}'".format(num_1,num_2, -(int(num_1)) + -(int(num_2)))
print(result)

